#!/usr/bin/env python3
"""Generate STRUCTURE.txt deterministically from the Git worktree.

The file is a tree-style listing of every tracked and untracked
(non-ignored) path — the shape the repository will have once pending
files are committed — so a freshly created file makes `--check` report
staleness immediately instead of one commit late.  Ignored paths (build
artifacts, local secrets, virtualenvs) can never appear.  Directories
are derived from the listed paths; the output is sorted bytewise.

Usage:
    tools/gen-structure.py            # write STRUCTURE.txt
    tools/gen-structure.py --check    # exit 1 if STRUCTURE.txt is stale
"""

from __future__ import annotations

import subprocess
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
OUTPUT = REPO_ROOT / "STRUCTURE.txt"
HEADER = (
    "# Generated by tools/gen-structure.py from `git ls-files`.\n"
    "# Do not edit by hand; run `make check-structure` to validate,\n"
    "# and regenerate with `python3 tools/gen-structure.py`.\n"
)
# Tracked placeholder files that only pin empty directories.
EXCLUDED_BASENAMES = {".gitkeep"}


def tracked_files() -> list[str]:
    # --cached lists the index, --others --exclude-standard adds untracked
    # files that are not ignored: together, the post-commit shape.
    result = subprocess.run(
        ["git", "-C", str(REPO_ROOT), "ls-files", "-z",
         "--cached", "--others", "--exclude-standard"],
        capture_output=True, check=True,
    )
    names = {
        name.decode("utf-8")
        for name in result.stdout.split(b"\0")
        if name
    }
    # The output file always lists itself: on first generation it does not
    # exist yet, and without this the freshly written index is immediately
    # stale against a listing that now contains it.
    names.add(OUTPUT.name)
    return sorted(
        name for name in names
        if Path(name).name not in EXCLUDED_BASENAMES
    )


def render(paths: list[str]) -> str:
    lines: list[str] = [HEADER]
    emitted_dirs: set[str] = set()
    for path in paths:
        parts = path.split("/")
        for depth in range(len(parts) - 1):
            directory = "/".join(parts[: depth + 1])
            if directory not in emitted_dirs:
                emitted_dirs.add(directory)
                lines.append("  " * depth + parts[depth] + "/")
        lines.append("  " * (len(parts) - 1) + parts[-1])
    return "\n".join(lines) + "\n"


def main(argv: list[str]) -> int:
    check_only = argv == ["--check"]
    if argv and not check_only:
        print("usage: gen-structure.py [--check]", file=sys.stderr)
        return 2
    rendered = render(tracked_files())
    if check_only:
        current = OUTPUT.read_text() if OUTPUT.is_file() else ""
        if current != rendered:
            print(
                "STRUCTURE.txt is stale; regenerate with "
                "`python3 tools/gen-structure.py`",
                file=sys.stderr,
            )
            return 1
        print("structure_index=PASS")
        return 0
    OUTPUT.write_text(rendered)
    print(f"wrote {OUTPUT.relative_to(REPO_ROOT)} ({len(rendered.splitlines())} lines)")
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
